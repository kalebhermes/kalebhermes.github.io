class Power {
	constructor(frequency, types, description){
		this.frequency = frequency;
		this.types = types;
		this.description = description;
	};

	setFrequency(frequency) {
		this.frequency = frequency;
	}

	setTypes(types) {
		this.types = types;
	}

	setDescription(description) {
		this.description = description;
	}
}